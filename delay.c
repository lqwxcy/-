#include"delay.h"

/*********************************************************** 
函数名称： 	  void DelayUs(u16 t)
函数功能： 	  大致延时长度如下 T=tx2+5 uS			   
入口参数： 	  t
出口参数： 	  无
备 注： 
***********************************************************/ 

void DelayUs(u16 t)
{
	while(t--);
}

/*********************************************************** 
函数名称： 	  void DelayUs(u16 t)
函数功能： 	  大致延时长度如下 1ms			   
入口参数： 	  t
出口参数： 	  无
备 注： 
***********************************************************/ 
void DelayMs(u16 t)
{
	while(--t)
	{
		DelayUs(245);
		DelayUs(245);
	}
}




